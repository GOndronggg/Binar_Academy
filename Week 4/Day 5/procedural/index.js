const menghitungLuasSegitiga = (a, t) => 0.5 * a * t;

const menghitungPersegi = (s) => s * s;

const menghitungPersegiPanjang = (p, l) => p * l;

const menghitungLingkaran = (r) => 3.14 * r * r;

console.log(`luas segitiga ${menghitungLuasSegitiga(10, 10)}`);
console.log(`luas persegi ${menghitungPersegi(10, 10)}`);
console.log(`luas persegi panjang ${menghitungPersegiPanjang(10, 10)}`);
console.log(`luas lingkaran ${menghitungLingkaran(10, 10)}`);
